let currentLang = "es";
let currentMode = "company";

function applyLanguage(lang) {
  currentLang = lang;
  document.documentElement.lang = lang;
  document.querySelector("[data-lang-label]").textContent = lang.toUpperCase();

  document.querySelectorAll("[data-i18n]").forEach((node) => {
    const key = node.dataset.i18n;
    if (translations[lang][key]) {
      node.textContent = translations[lang][key];
    }
  });

  updateScreenMode();
}

function updateScreenMode() {
  const label = currentMode === "company"
    ? translations[currentLang].companyMode
    : translations[currentLang].soloMode;

  const screenMode = document.querySelector("[data-screen-mode]");
  const companyOnly = document.querySelector("[data-company-only]");

  if (screenMode) {
    screenMode.textContent = label;
  }

  if (companyOnly) {
    companyOnly.hidden = currentMode !== "company";
  }
}

function setup() {
  applyLanguage(currentLang);
  lucide.createIcons();

  const header = document.querySelector("[data-header]");
  const updateHeader = () => header.classList.toggle("scrolled", window.scrollY > 12);
  updateHeader();
  window.addEventListener("scroll", updateHeader, { passive: true });

  document.querySelector("[data-lang-toggle]").addEventListener("click", () => {
    applyLanguage(currentLang === "es" ? "en" : "es");
  });

  document.querySelectorAll("[data-mode]").forEach((button) => {
    button.addEventListener("click", () => {
      currentMode = button.dataset.mode;
      document.querySelectorAll("[data-mode]").forEach((item) => {
        item.classList.toggle("active", item === button);
      });
      updateScreenMode();
    });
  });
}

setup();
